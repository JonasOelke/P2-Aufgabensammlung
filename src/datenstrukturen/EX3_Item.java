package datenstrukturen;

public class EX3_Item {
	private String title;
	private String information;
	private int amount;
	
	public String getTitle() {
		return title;
	}

	public String getInformation() {
		return information;
	}

	public int getAmount() {
		return amount;
	}

	public EX3_Item(String title, String information, int amount) {
		super();
		this.title = title;
		this.information = information;
		this.amount = amount;
	}
	
	

}
